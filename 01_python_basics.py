# ---------------------------------------------------------------
# 1) משתנים וטיפוסי נתונים
# ---------------------------------------------------------------

x = 10           # מספר שלם (int)
y = 3.14         # מספר עשרוני (float)
name = "אבי"    # מחרוזת (str)
is_active = True # בוליאני (True/False)

print(x, y, name, is_active)

# בדיקת סוג
print(type(x))   # <class 'int'>
print(type(name))# <class 'str'>


# ---------------------------------------------------------------
# 2) פעולות מתמטיות והשוואה
# ---------------------------------------------------------------

a, b = 7, 3

print(a + b)   # חיבור → 10
print(a - b)   # חיסור → 4
print(a * b)   # כפל   → 21
print(a / b)   # חילוק (float) → 2.333...
print(a // b)  # חילוק שלם → 2
print(a % b)   # מודולו (שארית) → 1
print(a ** b)  # חזקה → 7³=343

# השוואות
print(a > b)   # True
print(a == b)  # False
print(a != b)  # True


# ---------------------------------------------------------------
# 3) מבני נתונים בסיסיים
# ---------------------------------------------------------------

# רשימה (list) – ניתנת לשינוי
fruits = ["apple", "banana", "cherry"]
fruits.append("date")    # הוספה
print(fruits[0])         # גישה לפי אינדקס
print(len(fruits))       # אורך

# טפל (tuple) – קבוע, אי אפשר לשנות
coords = (10, 20)
print(coords[1])

# סט (set) – אוסף ייחודי, אין כפילויות
colors = {"red", "blue", "red"}
print(colors)  # {'red', 'blue'}

# מילון (dict) – זוגות מפתח:ערך
student = {"name": "Dan", "age": 25}
print(student["name"])
student["grade"] = 90    # הוספת מפתח חדש


# ---------------------------------------------------------------
# 4) תנאים (if/elif/else)
# ---------------------------------------------------------------

age = 18
if age >= 18:
    print("בגיר")
elif age > 13:
    print("נער")
else:
    print("ילד")


# ---------------------------------------------------------------
# 5) לולאות (for/while)
# ---------------------------------------------------------------

# for על רשימה
for fruit in fruits:
    print("פרי:", fruit)

# range = טווח מספרים
for i in range(5):
    print("i:", i)

# while – רץ כל עוד התנאי מתקיים
count = 0
while count < 3:
    print("ספירה:", count)
    count += 1


# ---------------------------------------------------------------
# 6) פונקציות בסיסיות
# ---------------------------------------------------------------

def greet(name):
    """פונקציה שמחזירה מחרוזת ברכה"""
    return f"היי {name}, מה נשמע?"

print(greet("רוני"))

# פונקציה עם ערך ברירת מחדל
def add(x, y=5):
    return x + y

print(add(10))      # 15
print(add(10, 2))   # 12


